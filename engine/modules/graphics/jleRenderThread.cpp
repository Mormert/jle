#include "jleRenderThread.h"

void jleRenderThread::runOnRenderThread(const std::function<void()> &task)
{
    _renderQueue.enqueue(task);
}

void jleRenderThread::processRenderQueue()
{
    std::function<void()> task;
    while (_renderQueue.try_dequeue(task)) {
        task();
    }
}