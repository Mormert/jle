#pragma once

#include <string>

class jleLuaClassSerialization
{
public:
    template <class Archive>
    std::string save_minimal( Archive const & ) const
    { return luaClassName; }

    template <class Archive>
    void load_minimal( Archive const &, std::string const & value )
    { luaClassName = value; }

    std::string luaClassName;
};
